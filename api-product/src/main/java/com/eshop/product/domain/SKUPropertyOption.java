package com.eshop.product.domain;

public class SKUPropertyOption {
	private String id;
	private String skuId;
	private String propertyOptionId;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSkuId() {
		return skuId;
	}

	public void setSkuId(String skuId) {
		this.skuId = skuId;
	}

	public String getPropertyOptionId() {
		return propertyOptionId;
	}

	public void setPropertyOptionId(String propertyOptionId) {
		this.propertyOptionId = propertyOptionId;
	}

}
